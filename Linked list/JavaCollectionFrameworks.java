import java.util.LinkedList;
public class JavaCollectionFrameworks {
    //java collection framework
    public static void main(String args[]){
        //create
    LinkedList<Integer>ll = new LinkedList<>();
    
    //add
    ll.addLast(1);
    ll.addLast(2);
    ll.addFirst(12);
    ll.addFirst(0);
    // remove 
    ll.removeLast();
    ll.removeFirst();
    System.out.print(ll);
    
    
}
}
